// hàm Math.abs()
alert(Math.abs(-5.5)); // 5.5
alert(Math.abs(10));  //10
// Hàm Math.abs trả về giá trị tuyệt đối của số

//hàm Math.ceil()
alert(Math.ceil(4.4)); //5
alert(Math.ceil(5.8)); //6
// Hàm Math.ceil() trả về giá trị làm tròn lên của giá trị đã nhập

// hàm Math.floor()
alert(Math.floor(5.7)); //5
alert(Math.floor(6.1)); //6
// Hàm Math.floor() trả về giá trị làm tròn xuống của giá trị đã nhập

// hàm Math.round()
alert(Math.floor(10.1)); //10
alert(Math.floor(20.6)); //21
// Hàm Math.round() trả về giá trị làm tròn đến số nguyên gần nhất của giá trị đã nhập